    <p class="defaultTextStyle">
        These are impotant facts about Bryxe:</p><br>
    <ul>
        <li><p class="defaultTextStyle">Topics - you can create topics of interest such as <em>Sports</em> or <em>Technology</em>
                that your members can join</p></li>
