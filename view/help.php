    <p class="defaultTextStyle">
        <strong><a id="howTopWk"></a>How topics of interest work</strong></p>
    <p class="defaultTextStyle">Everyone is interested in something special, beyond wanting to find out about the latest
        news and see or share pictures with friends or people they like. So, it's nice to be able to have posts only
        related to your common special interests as well as general posts unrelated to anything specific. Bryxe's <i>Topics</i>
        lets you have posts only related to your special interests. Best of all, you can use the foundation of people
        who's general interests or activities you are viewing, to select and add topics they like and probably know a
        lot about. Viewers that are viewing your activities can do the same, adding their thoughts to your topics,
        making for a very rich and fun environment all around.</p><br>
    <p class="defaultTextStyle">How do topics work? After adding a topic, you select it and then only the contents of
        that topic of interest is shown. Other posts are not shown, until you change to view either general posts or
        another topic of interest.</p><br>

    <p class="defaultTextStyle">
        <strong><a id="howGrpWk"></a>How groups work</strong></p>
    <p class="defaultTextStyle">The most basic group is the public group. Everyone always is a public member and anyone
        can view the contents in there. On the other hand you the user, has full control over your private groups. You
        add members to whichever private group you wish to. When you do that, posts added to a private group can be seen
        only by members of that group.</p><br>
    <p class="defaultTextStyle">How does that work? Very simple. You select a group and after that, only the content of
        that group is shown. Contents of other posts placed in other groups are no longer shown, nor are the public
        group posts shown. You must select another group to see those posts. You only see your own posts, replies and
        reposts by other members to your posts. Posts of other members unrelated to your posts are not shown in the
        private posts. For that to happen, the other members need to add you to their groups.</p><br>

    <blockquote>
        <p class="defaultTextStyle">
            <strong>IMPORTANT</strong></p>
        <p class="defaultTextStyle">Your posts are added to your groups and you only see replies and reposts of members
            you added to your group when that group is selected. You and each member has full control over who can see
            their own private contents. That means that you cannot see the contents of the same members you added to
            your private group. For that to happen those members must add you to their own private groups.</p>
    </blockquote>

